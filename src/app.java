public class app {

    public int sum(int a, int b) {
        return a + b;
    }

    public int mult (int a, int b) {
        return a * b;
    }

    public int div (int a, int b) {
        return a / b;
    }

    public int rest (int a, int b, int c) {
        return a - b - c;
    }

    public int sumres (int a, int b, int c) {
        return a + b -c;
    }
}
